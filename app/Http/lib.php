function getSiteName()
{
    static $siteName = null;
    if ($siteName != null)
        return $siteName;
    $name = SystemVariable::where('key', '=', 'website_name')->first();
    if ($name && $name->value)
        $siteName = $name->value;
    else
        $siteName = 'Payments Hub';

    return $siteName;
}

