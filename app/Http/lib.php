    if (@strlen(trim($ipdata->geoplugin_countryCode)) == 2) {
        $countryname = $ipdata->geoplugin_countryName;
        $countries = getCountries();
        foreach ($countries as $k => $v) {
            if (strtolower($k) == strtolower($countryname)) {
                $info->countryname = $k;
                break;
            }
        }

        $timezonedata = @json_decode(file_get_contents("https://maps.googleapis.com/maps/api/timezone/json?location=" . $ipdata->geoplugin_latitude . "," . $ipdata->geoplugin_longitude . "&timestamp=" . time() . "&sensor=false"));

        if (isset($timezonedata->timeZoneId) && !empty($timezonedata->timeZoneId)) {
            $timezone = $timezonedata->timeZoneId;
            $timezones = getTimeZones();
            foreach ($timezones as $k => $v) {
                if (strtolower($k) == strtolower($timezone)) {
                    $info->timezone = $k;
                    break;
                }
            }
        }

        $countries = getNationalities();
        foreach ($countries as $k => $v) {
            if (strtolower($k) == strtolower($countryname)) {
                $info->nationality = $k;
                break;
            }
        }

        $countries = getCountryCodes();
        foreach ($countries as $k => $v) {
            if (is_array($v) && isset($v[$ipdata->geoplugin_countryCode])) {
                $info->countrycode = $v[$ipdata->geoplugin_countryCode]['value'];
                break;
            } else if ($k == $ipdata->geoplugin_countryCode) {
                $info->countrycode = $v['value'];
                break;
            }
        }

    }

    return $info;
}

